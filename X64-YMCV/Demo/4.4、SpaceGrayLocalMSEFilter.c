#include "../EGE_LCD/EGE_LCD.h"
#include "../YMCV/CONFIG/YMCV_PubType.h"
#include  "../ImageSource/imageSource.h"
#include"../YMCV/OPOBJ/YMCV_Creat.h"
#include"../YMCV/OPOBJ/YMCV_Free.h"
#include"../YMCV/CONFIG/YMCV_Mem.h"
#include"../YMCV/CORE/YMCV_HistCal.h"
/*******************************************************************************************
    *
    * 说明  该例程以 局部均方差滤波为例，展示了将一幅图像 进行磨皮的效果，如果想用美颜效果建议磨皮后再加一层锐化
    *
    ***************************************************************************************/
int main()
{
    char myInform[100];
    LCD_Init(1000, 700);

    //为了不破坏原始数据，重新申请数据内存,并将数据封装为图片格式 
    uint8* myData = CV_malloc1(sizeof(mm01_gray));//必须采用CV_malloc1为图片数据申请内存
    CV_memcpy(myData, mm01_gray, sizeof(mm01_gray));
    CVIMAGE myImage0 = YMCV_Creat_DataToImg((uint8*)myData, 375, 500, CVGrayType);//灰度图
    //显示原图
    LCD_show_image(0, 0, myImage0);


    //创建空间渲染器
    uint8 kernel_r = 10;
    CVKERN_RENDER myKernel = YMCV_Creat_SpaceRender_Creat(CV_LMSEFilter, PIXChannelNum1, kernel_r, OutPaddingConst);
    YMCV_PADDING_VAL = 125;//设置填充值


    //加载渲染器参数，进行初始化
    CV_kernel_1param myParam = { .lms.sigma = 5.0, };

    //由于缓存结果
    CVIMAGE myImage1 = NULL;
    //半径可取：0.02*max(Width, Height)，
    //sigma可取5 + n ，其中n 为磨皮的程度参数，范围可取 1到10，越大磨皮越厉害。
    /*--------------------------局部均方差滤波 修改方差---------------------------------------------*/
    for (int i = 0; i < 10; i++)
    {
        //使用参数进行渲染器通道配置、初始化
        YMCV_Space_SingleKernel_Load(myKernel, &myParam);

        //使用空间渲染器进行渲染灰度图片
        myImage1 = YMCV_Image_Space_SigleKernel_Render(myImage0, myKernel);
        //显示图片1
        LCD_show_image(500, 0, myImage1);

        //用完就释放图像，避免下次用的时候导致内存泄漏
        YMCV_Free_ImgFree(myImage1);

        //信息显示
        sprintf(myInform, " 局部均方差滤波 sigma: %01.1f .", myParam.lms.sigma);
        LCD_ShowString(100, 510, myInform);
        //暂停
        PAUSE();

        //参数调整
        myParam.lms.sigma += 2.0f;
    }
    //释放渲染器
    YMCV_Free_KERN_RenderFree(myKernel);

    /*--------------------------局部均方差滤波 修改滤波半径--------------------------------------------------*/
    for (kernel_r = 0; kernel_r < 10; kernel_r++)
    {
        //创建相应半径的滤波核
        myKernel = YMCV_Creat_SpaceRender_Creat(CV_LMSEFilter, PIXChannelNum1, kernel_r, OutPaddingConst);

        //使用参数进行渲染器通道配置、初始化
        YMCV_Space_SingleKernel_Load(myKernel, &myParam);

        //使用空间渲染器进行渲染灰度图片
        myImage1 = YMCV_Image_Space_SigleKernel_Render(myImage0, myKernel);
        //显示图片1
        LCD_show_image(500, 0, myImage1);


        //信息显示
        sprintf(myInform, "局部均方差滤波 滤波半径: %02d   . ", kernel_r);
        LCD_ShowString(100, 510, myInform);
        //暂停
        PAUSE();

        //用释放图像，避免下次用的时候导致内存泄漏
        YMCV_Free_ImgFree(myImage1);
        //释放空间渲染器
        YMCV_Free_KERN_RenderFree(myKernel);
    }

    //使用完图片，要释放图片内存
    YMCV_Free_ImgFree(myImage0);

    PAUSE();
    LCD_Destory();
    return 0;
}





